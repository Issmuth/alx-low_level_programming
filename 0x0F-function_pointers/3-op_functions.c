#include "3-calc.h"

/**
 * op_add -  sums two numbers
 * @a: ...
 * @b: ...
 *
 * Return: the sum of the numbers
 */

int op_add(int a, int b)
{
	return ((a) + (b));
}

/**
 * op_sub - subtraction ooperation of two numbers
 * @a: ..
 * @b: ..
 *
 * Return: difference of the numbers
 */

int op_sub(int a, int b)
{
	return ((a) - (b));
}

/**
 * op_mul - multiplication ooperation of two numbers
 * @a: ..
 * @b: ..
 *
 * Return: product of the numbers
 */

int op_mul(int a, int b)
{
        return ((a) * (b));
}

/**
 * op_div - division operation of two numbers
 * @a: ..
 * @b: ..
 *
 * Return: quotient of the numbers
 */

int op_div(int a, int b)
{
        return ((a) / (b));
}

/**
 * op_mod - modulus operation of two numbers
 * @a: ..
 * @b: ..
 *
 * Return: division remainder of the numbers
 */

int op_mod(int a, int b)
{
        return ((a) % (b));
}
